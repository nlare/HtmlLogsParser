public class Event {

    private String Date;
    private String User;
    private String Path;

    public String getDate() {
        return Date;
    }

    public String getUser() {
        return User;
    }

    public String getPath() {
        return Path;
    }

    public void setDate(String date) {
        this.Date = date;
    }

    public void setUser(String user) {
        this.User = user;
    }

    public void setPath(String path) {
        this.Path = path;
    }
}
